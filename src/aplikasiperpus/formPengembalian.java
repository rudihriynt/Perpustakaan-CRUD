    private void btelatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btelatActionPerformed
        // TODO add your handling code here:
        try{
            DateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            Date tanggalpinjam = format.parse(tempo);
            Date tanggalkembali = format.parse(kembali);
            long tanggalpinjam1 = tanggalpinjam.getTime();
            long tanggalkembali1 = tanggalkembali.getTime();
            long diff = tanggalkembali1 - tanggalpinjam1;
            long lama = diff / (24 * 60 * 60 * 1000);
            telat.setEnabled(false);
            telat.setText(Long.toString(lama)+"");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Data Gagal Hitung"+e);
        }
    }//GEN-LAST:event_btelatActionPerformed

    
