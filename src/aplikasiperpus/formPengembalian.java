    private void bcari_pinjamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcari_pinjamActionPerformed
        // TODO add your handling code here:
        try {
            String sql="select * from peminjaman where kd_pinjam='"+ tcari.getText()+"'";
            st=con.createStatement();
            rspengembalian =st.executeQuery(sql);
            while (rspengembalian.next()) {
                String tp = rspengembalian.getString("tgl_pinjam");                
                java.util.Date tgl = new SimpleDateFormat("yyyy-MM-dd").parse(tp);
                dpinjam.setDate(tgl); 
                dpinjam.setEnabled(false);
                String kda = rspengembalian.getString("kd_anggota");
                kd_anggota.setText(kda);
                kd_anggota.setEnabled(false);
                String kdb = rspengembalian.getString("kd_buku");
                tkd2_kembali.setText(kdb);
                tkd2_kembali.setEnabled(false);
                String dp = rspengembalian.getString("tempo"); 
                java.util.Date tgl1 = new SimpleDateFormat("yyyy-MM-dd").parse(dp);
                dtempo.setDate(tgl1); 
                dtempo.setEnabled(false);
                String jmlh = rspengembalian.getString("jumlah");
                jumlah.setText(jmlh);
                jumlah.setEnabled(false);
            }            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_bcari_pinjamActionPerformed

    private void bsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsaveActionPerformed
        // TODO add your handling code here:
        String sql= "insert into pengembalian values (?,?,?,?)";
        try{
            PreparedStatement ps=con.prepareStatement(sql);
            ps.setString(1,kd_kembali.getText());
            ps.setString(2,tcari.getText());
            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            ps.setString(3,tanggal =format.format(dkembali.getDate()));
            ps.setString(4,telat.getText());
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosongkan();
            kd_kembali.requestFocus();
            tampilPengembalian();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+e);
        }
    }//GEN-LAST:event_bsaveActionPerformed

