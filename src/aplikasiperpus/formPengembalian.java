    private void tampilPengembalian(){
    Object[] Baris={"Kode Pengembalian","Kode Peminjaman","Tanggal Pengembalian","Telat/Hari"};
      grid =new DefaultTableModel(null, Baris);
      table_pengembalian.setModel(grid);
      String sql= "select * from pengembalian";
      try {
          java.sql.Statement stat = con.createStatement();
          ResultSet hasil= stat.executeQuery(sql);
            while (hasil.next()){
                String a = hasil.getString("kd_pengembalian");
                String b = hasil.getString("kd_peminjaman");
                String c = hasil.getString("tgl_kembali");
                String d = hasil.getString("telat");
                
                String[] data={a,b,c,d};
                grid.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null , "Gagal Tampil " +e);
        }
    }
    
