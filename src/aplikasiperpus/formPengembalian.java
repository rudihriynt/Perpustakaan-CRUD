    private void bupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bupdateActionPerformed
        // TODO add your handling code here:
        String sql="update pengembalian set kd_peminjaman=?, tgl_kembali=?,telat=? where kd_pengembalian=?";
        try{
            PreparedStatement st= con.prepareStatement(sql);
            st.setString(1,tcari.getText());
            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            st.setString(2,tanggal =format.format(dkembali.getDate()));
            st.setString(3,telat.getText());
            st.setString(4,kd_kembali.getText());
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diupdate");
            kosongkan();
            kd_kembali.requestFocus();
            tampilPengembalian();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal di Update");
        }
    }//GEN-LAST:event_bupdateActionPerformed

